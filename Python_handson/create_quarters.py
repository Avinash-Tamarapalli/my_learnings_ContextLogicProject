t=('January','February','March','April','May','June','July','August','September','October','November','December')
print("Months in expanded form:")
for i in t:
    print(i)
print("")
print("The four quarters are:\n")
print("First Quarter:")
for i in t[0:3]:
    print(i)
print("")
print("Second Quarter:")
for i in t[3:6]:
    print(i)
print("")
print("Third Quarter:")
for i in t[6:9]:
    print(i)
print("")
print("Fourth Quarter:\n")
for i in t[9:12]:
    print(i)
